package day04_wrapperClasses_Increment;

public class C05_preIncrement_postIncrement {
    public static void main(String[] args) {

        int a =10;

        a++;

        System.out.println(a);

        a =10;

        ++a;

        System.out.println(a);


        int b = 10;
        System.out.println("b : " + b);
        System.out.println("b++ : " + b++);
        System.out.println("b : " + b);
        System.out.println("++b : " + ++b);



    }
}
